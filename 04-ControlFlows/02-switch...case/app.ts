let carModel: string = 'Ferrari';

switch (carModel) {
case 'Ferrari':
        console.log('Ferrari is a cool car!');
        break;
    case 'Porsche':
        console.log('Porsche is a cool car!');
        break;
    case 'Lamborghini':
        console.log('Lamborghini is a cool car!');
        break;
    default:
        console.log('I do not know that car.');
}

